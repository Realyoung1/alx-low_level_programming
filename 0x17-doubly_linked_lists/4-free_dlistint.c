/**
 * free_dlistint - function that free a dlistint_t list.
 * @head: pointers to the beginning of the linked lists.
 *
 * Return: void
 */

void free_dlistint(dlistint_t *head)
{
	dlistint_t *next;

	while (head != NULL)
	{
		next = head->next;
		free(head);
		head = next;
	}
}
